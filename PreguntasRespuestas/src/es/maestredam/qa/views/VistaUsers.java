    private void setDatos(){
        LinkedList<Usuario> lista = gestor.cargarArchivo();
        Object[] datos = new Object[dtmUsuarios.getColumnCount()];
        Usuario user;
        for (int i = 0; i < lista.size(); i++) {
            user=lista.get(i);
            datos[0] = user.getNombre();
            datos[1] = user.getNumJugadas();
            datos[2] = user.getPuntuacionTotal();
            dtmUsuarios.addRow(datos);
        }
    }

