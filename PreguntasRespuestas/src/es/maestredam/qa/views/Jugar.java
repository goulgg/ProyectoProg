    private void btnConfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfActionPerformed
        seguir = true;
        int extraida;
        int indice;
        contador++;
        if(pregunta.getRespuestaB()==null){
            if(pregunta.compruebaNormal(txtResp.getText(), pregunta) == true){
                puntuacion = Integer.parseInt(txtPunt.getText());
                puntuacion++;
                txtPunt.setText(""+puntuacion);
            }
        }else{
            if(rdoA.isSelected()){
                if(pregunta.compruebaTest(rdoA.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPunt.getText());
                    puntuacion+=1;
                    txtPunt.setText(""+puntuacion);
                }
            }else if(rdoB.isSelected()){
                if(pregunta.compruebaTest(rdoB.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPunt.getText());
                    puntuacion+=1;
                    txtPunt.setText(""+puntuacion);
                }
            }else if(rdoC.isSelected()){
                if(pregunta.compruebaTest(rdoC.getText(), pregunta)){
                    puntuacion = Integer.parseInt(txtPunt.getText());
                    puntuacion+=1;
                    txtPunt.setText(""+puntuacion);
                }
            }  
        }
        extraida = (pregunta.getVecesExtraida())+1;
        indice = lista.indexOf(pregunta);
        lista.get(indice).setVecesExtraida(extraida);
        gestor.escribirLista(lista, false);
        getPregunta();
    }//GEN-LAST:event_btnConfActionPerformed

