    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        user = new Usuario(txtNombre.getText(),txtNombCom.getText(),txtClave.getText(),chkAdmin(chkAdmin));
        Usuario comparador;
        boolean existe=false;
        LinkedList<Usuario> lista;
        GestorInfo gestor = new GestorInfo("usuarios.dam");
        lista = gestor.cargarArchivo();
        if(lista.size()==0){
            lista.add(user);
            gestor.escribirLista(lista, false);
                dispose();
        }else{
            for (int i = 0; i < lista.size(); i++) {
                comparador=lista.get(i);
                if(user.getNombre().equals(comparador.getNombre())){
                    existe=true;
                }
            }
            if(existe){
                lblRepeUsu.setVisible(true);
            }else{
                lista.add(user);
                gestor.escribirLista(lista, false);
                dispose();
            }
        }
    }//GEN-LAST:event_btnCrearActionPerformed
    private boolean chkAdmin(JCheckBox cb1){
        boolean admin=false;
        if(cb1.isSelected()){
            admin=true;
        }
        return admin;
    }
    
